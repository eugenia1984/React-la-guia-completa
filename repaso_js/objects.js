/**** Objects ****/
// Object Literal
// Se declara entre {}, tiene key:value
const persona = {
  nombre: "Juan",
  profesion: "Desarrollador web",
  edad: 40,
  isWorking: true
}

// muestro todo el objeto
console.log(persona);

// accedo a un valor con dot notation
console.log(persona.nombre); // Juan

// accedo a un valor con los []
console.log(persona["edad"]); // 40
